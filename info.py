Uranus_info='''Uranus is perhaps the strangest planet in the solar system.
At some point during its history, the ice giant was knocked over,
leaving it spinning on its side. 
More than a dozen rings circle the world, and some 27 moons cling to it. 
The planet’s atmosphere is a collection of hydrogen, helium,
and heavier compounds that exist as ices deep in the frigid Uranian clouds.
But beyond a handful of intriguing facts,
scientists know woefully little about this milky blue world,
which was visited for the first and only time by the Voyager 2 spacecraft in 1986.
That could be about to change.'''


Neptune_info='''Dark, cold, and whipped by supersonic winds, 
ice giant Neptune is the eighth and most distant planet in our solar system.
More than 30 times as far from the Sun as Earth,
Neptune is the only planet in our solar system not visible to the naked eye and
the first predicted by mathematics before its discovery.
In 2011 Neptune completed its first 165-year orbit since its discovery in 1846.
NASA's Voyager 2 is the only spacecraft to have visited Neptune up close.
It flew past in 1989 on its way out of the solar system.'''


